   public void DFV(String consulta)
    {
        int DiasF=0;
      try {
             DefaultTableModel tabladvc=new DefaultTableModel();
             Con=DriverManager.getConnection(Globales.Url,Globales.User,Globales.Pass);
             St=Con.createStatement();
             Rs=St.executeQuery(consulta);
             tabladvc.setColumnIdentifiers(new Object[]{"ID_Per","Nombre","Apellido Paterno","Apellido Materno","Fecha Venci","Dias Faltantes"});
             while(Rs.next()){
               
               tabladvc.addRow(new Object[]{Rs.getString(1),Rs.getString(2),Rs.getString(3),Rs.getString(4),Rs.getString(5),Rs.getString(6)}); 
             }
             
            jTable4.setModel(tabladvc);
             
             St.close();
             Rs.close();
             Con.close();
        }catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Error: "+e);
        }    
    }
      
     
    
